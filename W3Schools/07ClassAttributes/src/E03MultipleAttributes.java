public class E03MultipleAttributes {
    String fName = "John";
    String lName = "Doe";
    int age = 24;

    public static void main(String[] args) {
        E03MultipleAttributes myObj = new E03MultipleAttributes();
        System.out.println("Name: " + myObj.fName + " " + myObj.lName);
        System.out.println("Age: " + myObj.age);
    }
}
